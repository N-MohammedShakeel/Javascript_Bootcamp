/*
    ===================
    Number in JavaScript
    ===================
    - JavaScript has only one number type (no int/float difference).
    - Numbers can be integers, decimals, +Infinity, -Infinity, NaN.
*/

let num1 = 42;
let num2 = 3.14;
let num3 = Infinity;
let num4 = NaN; // Not a Number (result of invalid math)

console.log(num1, num2, num3, num4);
