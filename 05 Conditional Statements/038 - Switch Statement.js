/*
    ==============================
    switch Statement
    ==============================
    - Used when we have multiple cases for a single variable.
*/

let day = 3;

switch (day) {
  case 1:
    console.log("Monday");
    break;
  case 2:
    console.log("Tuesday");
    break;
  case 3:
    console.log("Wednesday");
    break;
  case 4:
    console.log("Thursday");
    break;
  default:
    console.log("Other day");
}
